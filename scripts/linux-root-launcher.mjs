import { chmod, lstat, rename, writeFile } from "node:fs/promises";
import { basename, join } from "node:path";

export const LINUX_ELECTRON_SUFFIX = "-electron";

function assertExecutableName(executableName) {
  if (typeof executableName !== "string" || !/^[A-Za-z0-9._-]+$/.test(executableName) || basename(executableName) !== executableName) {
    throw new Error("Kyrei Linux launcher requires a safe executable name");
  }
}

async function pathExists(path) {
  try {
    await lstat(path);
    return true;
  } catch (error) {
    if (error && typeof error === "object" && error.code === "ENOENT") return false;
    throw error;
  }
}

/**
 * Keep Electron's Chromium sandbox enabled.  Electron aborts before the main
 * process starts when invoked as root, so the guard must live in the packaged
 * shell launcher rather than in electron/main.js.
 */
export function createLinuxRootGuardLauncher(executableName) {
  assertExecutableName(executableName);
  const electronExecutable = `${executableName}${LINUX_ELECTRON_SUFFIX}`;
  return `#!/usr/bin/env sh
# Generated by Kyrei's Linux packaging hook. Do not add --no-sandbox here.
if [ "$(id -u)" -eq 0 ]; then
  cat >&2 <<'KYREI_ROOT_LAUNCH_MESSAGE'
Kyrei must run as your regular desktop user, not through sudo or a root shell.
Kyrei нужно запускать от обычного пользователя, а не через sudo или root-shell.

Use sudo only to install or update a package. Then start Kyrei with:
  kyrei

For an AppImage, make the file executable and start it as your regular user.
Do not add --no-sandbox: Kyrei keeps Chromium's sandbox enabled.
Не добавляйте --no-sandbox: Kyrei сохраняет включённую песочницу Chromium.
KYREI_ROOT_LAUNCH_MESSAGE
  exit 1
fi

for ARG in "$@"; do
  case "$ARG" in
  --no-sandbox|--no-sandbox=*)
    cat >&2 <<'KYREI_NO_SANDBOX_MESSAGE'
Kyrei refused --no-sandbox because it would disable Chromium's security sandbox.
Kyrei не запускает --no-sandbox, потому что он отключает защитную песочницу Chromium.

Use the DEB or Arch package, or enable unprivileged user namespaces on this
Linux system. Do not work around this by disabling the sandbox.
Используйте DEB/Arch-пакет или включите непривилегированные user namespaces в
Linux. Не отключайте песочницу как обходной путь.

If this appeared while starting an AppImage, its runtime detected that user
namespaces are unavailable and attempted to add this unsafe flag. Kyrei exits
before Electron instead; install the DEB/Arch package or enable user namespaces.
Если это появилось при запуске AppImage, его runtime обнаружил недоступные user
namespaces и попытался добавить небезопасный флаг. Kyrei завершился до Electron:
установите DEB/Arch-пакет или включите user namespaces.
KYREI_NO_SANDBOX_MESSAGE
    exit 1
    ;;
  esac
done

# Graphical session required (TTY-only has neither display).
if [ -z "\${DISPLAY:-}" ] && [ -z "\${WAYLAND_DISPLAY:-}" ]; then
  cat >&2 <<'KYREI_NO_DISPLAY_MESSAGE'
Kyrei needs a graphical desktop session (DISPLAY or WAYLAND_DISPLAY is empty).
Kyrei нужна графическая сессия (нет DISPLAY / WAYLAND_DISPLAY).

Start Kyrei from your desktop menu or from a terminal inside the logged-in
Wayland/X11 session — not from a plain TTY or SSH without display forwarding.
Запустите Kyrei из меню DE или из терминала внутри графической сессии.
KYREI_NO_DISPLAY_MESSAGE
  exit 1
fi

# Wayland: prefer Electron Ozone auto when the user has not set a hint.
# Forces nothing if ELECTRON_OZONE_PLATFORM_HINT is already set (user override).
# auto → native Wayland when possible, XWayland otherwise (common on Hyprland/Sway).
if [ -n "\${WAYLAND_DISPLAY:-}" ] || [ "\${XDG_SESSION_TYPE:-}" = "wayland" ]; then
  if [ -z "\${ELECTRON_OZONE_PLATFORM_HINT+x}" ]; then
    export ELECTRON_OZONE_PLATFORM_HINT=auto
  fi
fi

APP_DIR=$(CDPATH= cd -- "$(dirname -- "$0")" && pwd -P) || exit 1
ELECTRON_BIN="$APP_DIR/${electronExecutable}"
if [ ! -x "$ELECTRON_BIN" ]; then
  cat >&2 <<KYREI_MISSING_BIN_MESSAGE
Kyrei electron binary is missing or not executable:
  $ELECTRON_BIN

Reinstall the package (Arch: sudo pacman -U ./Kyrei-*-x64.pkg.tar.zst).
Бинарник Electron не найден или без +x — переустановите пакет.
KYREI_MISSING_BIN_MESSAGE
  exit 1
fi

exec "$ELECTRON_BIN" "\$@"
`;
}

/**
 * Replace the generated Linux Electron entry point with a tiny user-facing
 * guard.  All Linux targets package appOutDir unchanged afterwards, so this
 * covers Debian, pacman, and AppImage launch paths with one implementation.
 */
export async function installLinuxRootGuard({ appOutDir, executableName }) {
  assertExecutableName(executableName);
  const launcherPath = join(appOutDir, executableName);
  const electronPath = join(appOutDir, `${executableName}${LINUX_ELECTRON_SUFFIX}`);

  if (!(await pathExists(electronPath))) {
    if (!(await pathExists(launcherPath))) {
      throw new Error(`Kyrei Linux executable was not found: ${launcherPath}`);
    }
    await rename(launcherPath, electronPath);
  }

  await writeFile(launcherPath, createLinuxRootGuardLauncher(executableName), { mode: 0o755 });
  await chmod(launcherPath, 0o755);
}
